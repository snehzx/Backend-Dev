import mongoose from "mongoose";

const hospitalSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    address2: {
      type: String,
    },
    city: {
      type: String,
      required: true,
    },
    pincode: {
      type: String,
      required: true,
    },
    specialization: [
      {
        type: String,
      },
    ],
  },
  { timestamps: true }
);

export const hospital = mongoose.model("hospital", hospitalSchema);
